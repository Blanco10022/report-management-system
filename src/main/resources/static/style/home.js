document.addEventListener('DOMContentLoaded', function() {
  var addButton = document.querySelector('.add-button');
  var userFormsContainer = document.querySelector('.user-forms');

  addButton.addEventListener('click', function() {
    var clonedUserForms = userFormsContainer.cloneNode(true);
    userFormsContainer.parentNode.insertBefore(clonedUserForms, userFormsContainer.nextSibling);
  });

  var textareaFields = document.querySelectorAll('.textarea-field');
  textareaFields.forEach(function(textareaField) {
    textareaField.addEventListener('input', function() {
      this.style.height = 'auto';
      this.style.height = this.scrollHeight + 'px';
    });
  });
});
